    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String eids=id.getText();
        if(eids.equals(""))
        {
            JOptionPane.showMessageDialog(null,"please enter the patient id");
        }
        else
        {
            Connection con=global.getCon();
            String query="select * from patient where id=?";
            try
        {
            int num=0;
           PreparedStatement pst=con.prepareStatement(query);
           pst.setString(1,eids);
           ResultSet rs=pst.executeQuery();
           if(rs.next()) {
                num=rs.getRow();
            }
           if(num>=1)
           {
               String query1="delete from patient where id=?";
             
                PreparedStatement pst1=con.prepareStatement(query1);
                pst1.setString(1,eids);
                pst1.executeUpdate();
                JOptionPane.showMessageDialog(null,"patient deleted");
             }
           else
           {
              JOptionPane.showMessageDialog(null,"patient Record not found");
     
           }
         }  
            catch(SQLException | HeadlessException e)
            {
             JOptionPane.showMessageDialog(null,e.getMessage());   
            }
            
            
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

